package com.juegito.model;

/**
 * Tipos de biomas en el juego.
 * Cada bioma tiene efectos únicos sobre el gameplay.
 */
public enum BiomeType {
    /**
     * Bosque: Proporciona bonificación a la defensa.
     */
    FOREST,
    
    /**
     * Montaña: Dificulta el movimiento pero proporciona ventaja en altura.
     */
    MOUNTAIN,
    
    /**
     * Llanura: Terreno neutral sin modificadores especiales.
     */
    PLAINS
}
